#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t	i;

	i = 0;
	if (n == 0)
		return (0);
	while (n--)
		if (*(int *)(s1 + i) == *(int *)(s2 + i))
			i++;
	return (*(int *)s1 - *(int *)s2);
}
